import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const appRoot = path.join(path.dirname(fileURLToPath(import.meta.url)), "..");

function loadEnvFile(filePath) {
  const env = {};
  try {
    for (const line of readFileSync(filePath, "utf8").split("\n")) {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith("#")) continue;
      const eq = trimmed.indexOf("=");
      if (eq === -1) continue;
      const key = trimmed.slice(0, eq).trim();
      let value = trimmed.slice(eq + 1).trim();
      if (
        (value.startsWith('"') && value.endsWith('"')) ||
        (value.startsWith("'") && value.endsWith("'"))
      ) {
        value = value.slice(1, -1);
      }
      env[key] = value;
    }
  } catch {
    // optional file
  }
  return env;
}

const env = {
  ...loadEnvFile(path.join(appRoot, ".env")),
  ...loadEnvFile(path.join(appRoot, ".env.local")),
};

const basePath = (env.NEXT_PUBLIC_BASE_PATH ?? "").replace(/\/$/, "");
const withBasePath = (path) => `${basePath}${path}`;

const config = {
  apiKey: env.NEXT_PUBLIC_FIREBASE_API_KEY ?? "",
  authDomain: env.NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN ?? "",
  projectId: env.NEXT_PUBLIC_FIREBASE_PROJECT_ID ?? "",
  storageBucket: env.NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET ?? "",
  messagingSenderId: env.NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID ?? "",
  appId: env.NEXT_PUBLIC_FIREBASE_APP_ID ?? "",
};

const sw = `/* eslint-disable no-undef */
/* Generated by scripts/generate-firebase-sw.mjs — do not edit; set NEXT_PUBLIC_FIREBASE_* in .env.local */
importScripts("https://www.gstatic.com/firebasejs/10.7.1/firebase-app-compat.js");
importScripts("https://www.gstatic.com/firebasejs/10.7.1/firebase-messaging-compat.js");

firebase.initializeApp(${JSON.stringify(config, null, 2)});

const messaging = firebase.messaging();

function resolveNotificationUrl(data) {
  if (data?.url) {
    if (data.url.startsWith("http")) {
      return data.url;
    }
    return new URL(data.url, self.location.origin).href;
  }
  if (data?.snapshot_id) {
    return new URL(\`${basePath}/runs/\${data.snapshot_id}\`, self.location.origin).href;
  }
  return new URL(\`${basePath || "/"}\`, self.location.origin).href;
}

messaging.onBackgroundMessage((payload) => {
  const title = payload.notification?.title ?? "Volume scan alert";
  const options = {
    body: payload.notification?.body ?? "",
    icon: "${withBasePath("/file.svg")}",
    data: payload.data,
    tag: \`volume-scan-\${payload.data?.snapshot_id || "daily"}\`,
  };
  self.registration.showNotification(title, options);
});

self.addEventListener("notificationclick", (event) => {
  event.notification.close();
  const targetUrl = resolveNotificationUrl(event.notification.data);
  event.waitUntil(
    clients.matchAll({ type: "window", includeUncontrolled: true }).then((windowClients) => {
      for (const client of windowClients) {
        if (client.url.startsWith(self.location.origin) && "focus" in client) {
          client.navigate(targetUrl);
          return client.focus();
        }
      }
      if (clients.openWindow) {
        return clients.openWindow(targetUrl);
      }
      return undefined;
    }),
  );
});
`;

writeFileSync(path.join(appRoot, "public/firebase-messaging-sw.js"), sw);
console.log("[generate-firebase-sw] Wrote public/firebase-messaging-sw.js");
